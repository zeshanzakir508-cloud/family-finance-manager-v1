/// The direction of the animation.
enum AnimationDirection {
  /// From top to bottom.
  fromTop,

  /// From bottom to top.
  fromBottom,

  /// From left to right.
  fromLeft,

  /// From right to left.
  fromRight,

  /// From center outward.
  fromCenter,

  /// From outward to center.
  toCenter,
}
