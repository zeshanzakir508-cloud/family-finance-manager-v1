// lib/presentation/widgets/animations/enums/transition_type.dart

/// The type of page transition.
enum TransitionType {
  /// Fade transition.
  fade,

  /// Scale transition.
  scale,

  /// Slide from bottom.
  slideBottom,

  /// Slide from top.
  slideTop,

  /// Slide from right.
  slideRight,

  /// Slide from left.
  slideLeft,

  /// Zoom transition.
  zoom,

  /// Rotation transition.
  rotation,

  /// No transition.
  none,
}
