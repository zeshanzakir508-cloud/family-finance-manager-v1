/// The size of the button.
///
/// Each size corresponds to specific height, padding, and text style
/// values defined in the button constants and helpers.
enum AppButtonSize {
  /// Extra small button (28px height) - for compact spaces.
  extraSmall,

  /// Small button (32px height) - for secondary actions.
  small,

  /// Medium button (40px height) - default size for most actions.
  medium,

  /// Large button (48px height) - for primary actions and emphasis.
  large,

  /// Extra large button (56px height) - for prominent actions.
  extraLarge,
}
