/// The size of the Floating Action Button (FAB).
///
/// Each size corresponds to specific dimensions defined
/// in the FAB style builder and constants.
enum AppFabSize {
  /// Small FAB (40px diameter) - for compact spaces.
  small,

  /// Regular FAB (56px diameter) - default size.
  regular,

  /// Large FAB (64px diameter) - for prominent actions.
  large,

  /// Extended FAB - wider with text and icon.
  extended,
}
