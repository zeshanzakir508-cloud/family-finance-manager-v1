/// The visual variant of the icon button.
///
/// Each variant corresponds to a specific visual treatment
/// for icon buttons.
enum AppIconButtonVariant {
  /// Standard transparent icon button - no background.
  standard,

  /// Filled icon button - solid background with primary color.
  filled,

  /// Filled tonal icon button - tonal background with primary color.
  filledTonal,

  /// Outlined icon button - border with transparent background.
  outlined,
}
