/// The animation type for charts.
enum ChartAnimation {
  /// No animation.
  none,

  /// Fade in animation.
  fade,

  /// Scale from bottom animation.
  scale,

  /// Grow from center animation.
  grow,

  /// Slide from left animation.
  slideLeft,

  /// Slide from right animation.
  slideRight,
}
