/// The position of data labels on charts.
enum ChartLabelPosition {
  /// Labels above data points.
  top,

  /// Labels below data points.
  bottom,

  /// Labels inside data points.
  inside,

  /// Labels at the end of bars.
  end,

  /// No labels displayed.
  none,
}
