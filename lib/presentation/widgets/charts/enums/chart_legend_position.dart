/// The position of the chart legend.
enum ChartLegendPosition {
  /// Legend above the chart.
  top,

  /// Legend below the chart.
  bottom,

  /// Legend to the left of the chart.
  left,

  /// Legend to the right of the chart.
  right,

  /// Legend overlays the chart.
  overlay,

  /// No legend displayed.
  none,
}
