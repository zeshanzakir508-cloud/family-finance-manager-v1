// lib/presentation/widgets/charts/enums/chart_type.dart

/// The type of chart to display.
enum ChartType {
  /// Line chart for trends over time.
  line,

  /// Bar chart for comparing values.
  bar,

  /// Horizontal bar chart for category comparison.
  horizontalBar,

  /// Pie chart for proportional data.
  pie,

  /// Donut chart for proportional data with a hole.
  donut,

  /// Area chart for cumulative trends.
  area,

  /// Scatter chart for correlation analysis.
  scatter,

  /// Sparkline for mini trend indicators.
  sparkline,
}
