/// The animation type for overlays.
enum OverlayAnimation {
  /// Fade in animation.
  fade,

  /// Scale in from center animation.
  scale,

  /// Slide from bottom animation.
  slideBottom,

  /// Slide from top animation.
  slideTop,

  /// Slide from left animation.
  slideLeft,

  /// Slide from right animation.
  slideRight,

  /// Bounce animation.
  bounce,

  /// No animation.
  none,
}
