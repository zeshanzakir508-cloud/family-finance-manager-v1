/// The position of the overlay relative to its anchor.
enum OverlayPosition {
  /// Display above the anchor.
  top,

  /// Display below the anchor.
  bottom,

  /// Display to the left of the anchor.
  left,

  /// Display to the right of the anchor.
  right,

  /// Display centered on the anchor.
  center,

  /// Display at the top-left of the anchor.
  topLeft,

  /// Display at the top-right of the anchor.
  topRight,

  /// Display at the bottom-left of the anchor.
  bottomLeft,

  /// Display at the bottom-right of the anchor.
  bottomRight,
}
