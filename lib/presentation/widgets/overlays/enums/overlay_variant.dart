/// The visual variant of the overlay.
enum OverlayVariant {
  /// Primary colored overlay.
  primary,

  /// Secondary colored overlay.
  secondary,

  /// Surface colored overlay.
  surface,

  /// Tonal overlay with container background.
  tonal,

  /// Transparent overlay.
  transparent,
}
