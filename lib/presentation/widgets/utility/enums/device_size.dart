/// The size category of the device.
enum DeviceSize {
  /// Small phone (under 360px width).
  smallPhone,

  /// Medium phone (360-400px width).
  mediumPhone,

  /// Large phone (400-600px width).
  largePhone,

  /// Tablet (600-1024px width).
  tablet,

  /// Desktop (1024px+ width).
  desktop,

  /// Large desktop (1440px+ width).
  largeDesktop,
}
